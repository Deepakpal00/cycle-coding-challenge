package com.nintyone.social.configurations;

public class CycleConfigurations {
	public int frameType = 0;
	public int tubetype = 0;
	public int noOfGears = 0;
	public int handleBarwithbrakes = 1;
	public int seating = 1;
	public int wheels = 2;
	public int spokes = 0;
	public int rim = 2;
	public int tube = 2;
	public String tyreType = "";

	/**
	 * @return the frameType
	 */
	public int getFrameType() {
		return frameType;
	}

	/**
	 * @param frameType the frameType to set
	 */
	public void setFrameType(int frameType) {
		this.frameType = frameType;
	}

	/**
	 * @return the tubetype
	 */
	public int getTubetype() {
		return tubetype;
	}

	/**
	 * @param tubetype the tubetype to set
	 */
	public void setTubetype(int tubetype) {
		this.tubetype = tubetype;
	}

	/**
	 * @return the noOfGears
	 */
	public int getNoOfGears() {
		return noOfGears;
	}

	/**
	 * @param noOfGears the noOfGears to set
	 */
	public void setNoOfGears(int noOfGears) {
		this.noOfGears = noOfGears;
	}

	/**
	 * @return the handleBarwithbrakes
	 */
	public int getHandleBarwithbrakes() {
		return handleBarwithbrakes;
	}

	/**
	 * @param handleBarwithbrakes the handleBarwithbrakes to set
	 */
	public void setHandleBarwithbrakes(int handleBarwithbrakes) {
		this.handleBarwithbrakes = handleBarwithbrakes;
	}

	/**
	 * @return the seating
	 */
	public int getSeating() {
		return seating;
	}

	/**
	 * @param seating the seating to set
	 */
	public void setSeating(int seating) {
		this.seating = seating;
	}

	/**
	 * @return the wheels
	 */
	public int getWheels() {
		return wheels;
	}

	/**
	 * @param wheels the wheels to set
	 */
	public void setWheels(int wheels) {
		this.wheels = wheels;
	}

	/**
	 * @return the spokes
	 */
	public int getSpokes() {
		return spokes;
	}

	/**
	 * @param spokes the spokes to set
	 */
	public void setSpokes(int spokes) {
		this.spokes = spokes;
	}

	/**
	 * @return the rim
	 */
	public int getRim() {
		return rim;
	}

	/**
	 * @param rim the rim to set
	 */
	public void setRim(int rim) {
		this.rim = rim;
	}

	/**
	 * @return the tube
	 */
	public int getTube() {
		return tube;
	}

	/**
	 * @param tube the tube to set
	 */
	public void setTube(int tube) {
		this.tube = tube;
	}

	/**
	 * @return the tyreType
	 */
	public String getTyreType() {
		return tyreType;
	}

	/**
	 * @param tyreType the tyreType to set
	 */
	public void setTyreType(String tyreType) {
		this.tyreType = tyreType;
	}

}
